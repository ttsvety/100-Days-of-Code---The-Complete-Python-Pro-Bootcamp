# Subscripting
print("Hello"[-1:])
print("Hello"[-1])
print("Hello"[4])
print("Hello"[-5])
print("Hello"[0])
print("Hello"[:1])

# String
print("123" + "345") # Concatenation

# Integer = Whole number
print(123 + 345)

# Large Integers
print(123,456,789) # Different numbers
print(123456789)
print(123_456_789) # _separate the big numbers to visualize it more easily

# Float = Floating Point Number
print(3.14159)
a = 3.14159
print(f"{a :.2f}")
print("{:.2f}".format(a))
print("a=%d" %(a))
print("a=%f" %(a))
print("a=%.2f" %(a))

#Boolean
print(True)
print(False)